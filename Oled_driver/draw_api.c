uchar func_y(float k,unsigned char x1,uchar y1,uchar x)
{
	float a=k*(x-x1)+y1;
	float b=k*(x-x1)+y1+0.5;
	if((uchar)b>(uchar)a)
		return b;
	else
		return a;
}

uchar func_x(float k,uchar x1,uchar y1,uchar y)
{
	float a=(y-y1)/k+x1;
	float b=(y-y1)/k+x1+0.5;
	if((uchar)b>(uchar)a)
		return b;
	else
	{
		return a;
	}

}

